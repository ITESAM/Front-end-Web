const daysAgo = (days: number, hoursOffset = 0) => {
  const date = new Date();
  date.setHours(date.getHours() - hoursOffset - days * 24);
  return date.toISOString();
};

const generateHistorico = (status: string, days: number) => [
  {
    status,
    data: daysAgo(days),
    por: "Sistema automático",
  },
];

